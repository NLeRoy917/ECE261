// include standard lib
#include <stdio.h>

// create int main()
int main() {
  
  // declare an int
  int x = 10;
  
  // print out the int
  printf("the value of x %d\n", x);
  
  // declare a float
  float ten = 33.9;
  
  // print out the float
  printf("the value of float is %lf\n",ten);
  
  // declare a character
  char Z = 'Z';
  
  // print out the char
  printf("the char is  %c\n",Z);

  // return 0
  return 0;
}